-- Number
x = 5
y = 15.5

print(type(x))
print(type(y))

-- String

msg = "Ola mundo!"
print(type(msg))

-- Boolean
print(type(true))
print(type(1 > 2))

-- nil/null/nulo
print(type(ASD))
print(type(nil))

-- function
print(type(print))
print(type(type))

-- table/array
t = { 0, 1, 2, "texto" }
print(type(t))
print(t[2])
